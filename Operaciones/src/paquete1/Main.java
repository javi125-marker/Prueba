package paquete1;

public class Main {

	public static void main(String[] args) {
		
		
		//switch-case
		//int y char
		
		int x= '6';
		
		switch(x) {
		
		case 'a': 
		{
		System.out.println("Esta es la opcion 0");
		break;
		}
		case 'b':
		{
			System.out.println("Esta es la opcion 1");
			break;
		}
		default:
		{
			System.out.println("Esta es la opcion por defecto");
			System.out.println("Mi nombre es Javier");
		}

	}
		
  }

}
